    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed

        String textoRendaAnual = jTextFieldCampoRendaAnual.getText();
        String textoRendaMensal = jTextFieldCampoRendaMensal.getText();
        String textoIdade = jTextFieldCampoIdade.getText();
        String textoRenda = null;
        boolean mensal = false;

        if (textoRendaMensal.equals("")) {
            textoRenda = textoRendaAnual;
        } else if (textoRendaAnual.equals("")) {
            textoRenda = textoRendaMensal;
            mensal = true;
        } else {
            JOptionPane.showMessageDialog(this, "Digite apenas Renda anual OU Mensal!");
            return;
        }

        textoRenda = textoRenda.replace(",", ".");

        if (!textoRenda.equals("") || !textoIdade.equals("")) {

            try {
                NumberFormat formatter = NumberFormat.getCurrencyInstance();

                Double rendaAnual = Double.valueOf(textoRenda);
                if (mensal) {
                    rendaAnual = rendaAnual * 12;
                    jLabelAnual.setText(formatter.format(rendaAnual));
                }

                jLabelMensal.setText(formatter.format(rendaAnual / 12));

                Integer idade = Integer.valueOf(textoIdade);
                idade = Math.abs(idade);

                double subAcumulador = (idade * rendaAnual) / 10;
                double hiperAcumulador = subAcumulador * 2;
                double independente = rendaAnual / 0.04;

                jLabelSubCalc.setText(formatter.format(subAcumulador));
                jLabelHiperCalc.setText(formatter.format(hiperAcumulador));
                jLabelIndCalc.setText(formatter.format(independente));
                
                jLabelSubMeses.setText("" + (subAcumulador / (rendaAnual / 12)));
                jLabelHiperMeses.setText("" + (hiperAcumulador / (rendaAnual / 12)));
                mensal = false;

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            }

        } else {
            JOptionPane.showMessageDialog(this, "Campo vazio!");
        }


    }//GEN-LAST:event_jButtonCalcularActionPerformed

